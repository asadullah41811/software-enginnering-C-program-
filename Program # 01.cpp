//write the program to check the number is even or odd//
#include<iostream>
using namespace std;
int main()
{
	
	int num;
	cout<<"  Please enter the Number::"<<endl;
	cin>>num;
	
	if(num % 2 == 0)
		
	cout<<"The number you entered is  even:: \n"<<  num;
	else
	
		cout<<" The number you entered  is odd : \n"<< num;
	
	
		
		
	
	return 0;
	
	
};

